import fs from "fs";
import path from "path";
import ts from "typescript";

const root = process.cwd();
const inputPath = "./ui/components.ts";
const outputPath = "./sdk/components.ts";

const sourceText = fs.readFileSync(inputPath, "utf8");
const sourceFile = ts.createSourceFile(inputPath, sourceText, ts.ScriptTarget.Latest, true, ts.ScriptKind.TS);

const components = new Map();
const rawExports = new Set();

function isExported(node) {
    return !!node.modifiers?.some(mod => mod.kind === ts.SyntaxKind.ExportKeyword);
}

function getText(node) {
    return node.getText(sourceFile);
}

function getIdentifierName(node) {
    return ts.isIdentifier(node) ? node.text : null;
}

function getPropsFromCreateHostComponent(initializer) {
    if (!ts.isCallExpression(initializer)) return null;
    const expression = initializer.expression;
    if (!ts.isIdentifier(expression) || expression.text !== "createHostComponent") return null;
    return initializer.typeArguments?.[0] ? getText(initializer.typeArguments[0]) : null;
}

function getPropsFromArrowFunction(initializer) {
    if (!ts.isArrowFunction(initializer)) return null;
    const firstParam = initializer.parameters[0];
    if (!firstParam?.type) return null;
    return getText(firstParam.type);
}

function getPropsFromFunctionDeclaration(node) {
    const firstParam = node.parameters[0];
    if (!firstParam?.type) return null;
    return getText(firstParam.type);
}

function visit(node) {
    if (ts.isVariableStatement(node) && isExported(node)) {
        for (const declaration of node.declarationList.declarations) {
            const name = getIdentifierName(declaration.name);
            if (!name || !declaration.initializer) continue;

            let propsType = getPropsFromCreateHostComponent(declaration.initializer);
            if (!propsType) propsType = getPropsFromArrowFunction(declaration.initializer);

            if (propsType) {
                components.set(name, propsType);
                continue;
            }

            if (ts.isIdentifier(declaration.initializer)) {
                const target = declaration.initializer.text;
                if (components.has(target)) components.set(name, components.get(target));
                else rawExports.add(name);
                continue;
            }

            rawExports.add(name);
        }
    }

    if (ts.isFunctionDeclaration(node) && isExported(node) && node.name) {
        const name = node.name.text;
        const propsType = getPropsFromFunctionDeclaration(node);

        if (propsType) components.set(name, propsType);
        else rawExports.add(name);
    }

    ts.forEachChild(node, visit);
}

visit(sourceFile);

const ignoredRawExports = new Set(["default"]);
const componentEntries = [...components.entries()];
const rawEntries = [...rawExports].filter(name => !ignoredRawExports.has(name) && !components.has(name));

const lines = [];

lines.push(`// This file is generated by tools/generate-react-sdk.mjs.`);
lines.push(`// Do not edit this file by hand.`);
lines.push(``);
lines.push(`import * as Internal from "../ui/components";`);
lines.push(``);
lines.push(`export type * from "../ui/components";`);
lines.push(``);

for (const [name] of componentEntries) {
    lines.push(`export const ${name}: typeof Internal.${name} = Internal.${name};`);
}

if (rawEntries.length) {
    lines.push(``);

    for (const name of rawEntries) {
        lines.push(`export const ${name}: typeof Internal.${name} = Internal.${name};`);
    }
}

lines.push(``);
lines.push(`export default {`);

for (const [name] of componentEntries) lines.push(`    ${name},`);
for (const name of rawEntries) lines.push(`    ${name},`);

lines.push(`};`);
lines.push(``);

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, lines.join("\n"), "utf8");

console.log(`Generated ${path.relative(root, outputPath)}`);
console.log(`Components: ${componentEntries.map(([name]) => name).join(", ")}`);
console.log(`Raw exports: ${rawEntries.join(", ")}`);